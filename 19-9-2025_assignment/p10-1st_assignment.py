width = int(input("Enter the Width Of The Rectangular :")) # 4

lengh = int(input("Enter the lengh Of The Rectangular :")) # 3

height = int(input("Enter the height Of The Rectangular :")) # 6

area = (width * lengh) + (height * lengh) + (height * width) 
print (f"The area of a rectangular {area*2}")

